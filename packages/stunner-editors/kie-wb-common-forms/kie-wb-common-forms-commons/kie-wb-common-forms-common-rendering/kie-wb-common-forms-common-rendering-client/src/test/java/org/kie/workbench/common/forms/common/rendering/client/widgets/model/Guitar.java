package org.kie.workbench.common.forms.common.rendering.client.widgets.model;

public class Guitar {

    private String brand;
    private String model;
    private String body;
    private String neck;

    public Guitar() {
    }

    public Guitar(String brand,
                  String model,
                  String body,
                  String neck) {
        this.brand = brand;
        this.model = model;
        this.body = body;
        this.neck = neck;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getNeck() {
        return neck;
    }

    public void setNeck(String neck) {
        this.neck = neck;
    }
}
