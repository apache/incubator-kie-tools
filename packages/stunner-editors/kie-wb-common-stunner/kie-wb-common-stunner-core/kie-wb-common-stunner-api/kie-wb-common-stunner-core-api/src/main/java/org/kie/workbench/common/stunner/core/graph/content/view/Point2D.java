package org.kie.workbench.common.stunner.core.graph.content.view;

import java.util.Objects;

import org.jboss.errai.common.client.api.annotations.MapsTo;
import org.jboss.errai.common.client.api.annotations.Portable;

/**
 * The values for the X/Y coordinates on the cartesian coordinates system.
 */
@Portable
public final class Point2D {

    private double x;
    private double y;

    public static Point2D create(final double x,
                                 final double y) {
        return new Point2D(x,
                           y);
    }

    public static Point2D clone(final Point2D point) {
        return new Point2D(point.getX(),
                           point.getY());
    }

    public Point2D(final @MapsTo("x") double x,
                   final @MapsTo("y") double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public Point2D copy() {
        return new Point2D(x, y);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        Point2D point2D = (Point2D) o;
        return Double.compare(point2D.x, x) == 0 &&
                Double.compare(point2D.y, y) == 0;
    }

    @Override
    public int hashCode() {
        return Objects.hash(x, y);
    }

    @Override
    public String toString() {
        return "[" + x + ", " + y + "]";
    }
}
