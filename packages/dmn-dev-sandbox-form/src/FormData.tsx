import { NotificationSeverity } from "@kogito-tooling/notifications/dist/api";

export enum EvaluationStatus {
  SUCCEEDED = "SUCCEEDED",
  SKIPPED = "SKIPPED",
  FAILED = "FAILED",
}

export interface DecisionResultMessage {
  severity: NotificationSeverity;
  message: string;
  messageType: string;
  sourceId: string;
  level: string;
}

export type Result = boolean | number | null | object | object[] | string;

export interface DecisionResult {
  decisionId: string;
  decisionName: string;
  result: Result;
  messages: DecisionResultMessage[];
  evaluationStatus: EvaluationStatus;
}

export interface DmnResult {
  details?: string;
  stack?: string;
  decisionResults?: DecisionResult[];
  messages: DecisionResultMessage[];
}

export interface DmnFormSchema {
  definitions?: {
    InputSet?: {
      properties: object;
    };
  };
}

export interface FormData {
  filename: string;
  modelName: string;
  schema: DmnFormSchema;
  formUrl: string;
  modelUrl: string;
  swaggerUIUrl: string;
}

export const DATA_JSON_PATH = "/data.json";
